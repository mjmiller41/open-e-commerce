CREATE OR REPLACE FUNCTION "public"."get_product_average_rating"("p_id" bigint) RETURNS numeric
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
BEGIN
    RETURN (SELECT COALESCE(AVG(rating), 0) FROM public.reviews WHERE product_id = p_id);
END;
$$;


